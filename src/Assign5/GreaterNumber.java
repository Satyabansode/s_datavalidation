/* Program-6
Write the program to check whether the no is greater than 100. */
public class GreaterNumber {
	public static void main(String[] args) {
		int a = 198;
		if (a > 100) {
			System.out.println(a + ": is greater than 100");
		} else {
			System.out.println(a + ": is less than 100");
		}
	}
}
