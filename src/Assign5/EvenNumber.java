package Assign5;

/* Program-5
Write a program to check whether number is even or odd. */
public class EvenNumber {
	public static void main(String[] args) {
		int num = 3243;
		if (num % 2 == 0) {
			System.out.println(num + ": is even number");
		}
		System.out.println(num + ": is Odd number");
	}
}
